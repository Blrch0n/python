# Хэрэглэгчээс утгуудыг авч байна
P = float(input("Анхны мөнгөн хэмжээ (P)-г оруулна уу: "))
r = float(input("Хүүний хэмжээ (r) оруулна уу (жишээ: 0.08 = 8%): "))
n = int(input("Хүүг жилд тооцсон тоо (n)-г оруулна уу: "))
t = int(input("Жилийн тоо (t)-г оруулна уу: "))

# Эцсийн дүнг томъёо A = P * (1 + r/n)^(n*t) дагуу тооцоолно
A = P * (1 + r/n) ** (n * t)

# Эцсийн үр дүнг хэвлэнэ
print("Эцсийн дүн (A):", A)

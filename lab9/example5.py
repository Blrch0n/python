width = int(input("Өргөн? ")) #Өргөнийн string хэлбэрээр өгөөд int-ээр тоо болгоно.Өргөн нь.
height = int(input("Өндөр? ")) #Өргөнийн string хэлбэрээр өгөөд int-ээр тоо болгоно.Өндөр нь.
area = width * height #Тэгш өнцөгтийн талбайг энэ өндөр өргөнөөр үржүүлж олж байна.
print("Тэгш өнцөгтийн талбай: ", area) #Тэгш өнцөгтийн талбайг терминал дээр хэвлэж үзүүлж байна.

## Цельсийн градусыг фаренгейтийн градус болгон хөрвүүлэх програм ##
#---------------------------------------
# B221870408 У.Болорчулуун
#---------------------------------------

deg_c = int(input("Температурыг цельсийн градусаар оруул? "))
# C-ийг F-рүү хөрвүүлэх томъёо: (цельсийн градус) * (9/5) + (32)
deg_f = deg_c * (9 / 5) + 32
print("Цельсийн ", deg_c, " градус нь фаренгейтийн", deg_f, " градус болно.")